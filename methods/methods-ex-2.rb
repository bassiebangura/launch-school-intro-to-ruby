# What do the following expressions evaluate to?

1. x = 2    # => 2 NB: variable assignment returns the value assigned

2. puts x = 2    # => nil NB: puts prints the value and returns nil

3. p name = "Joe"    # => "Joe" NB: p is a method that prints the value of an expression and returns the value of the expression

4. four = "four"    # => "four" NB: variable assignment returns the value assigned

5. print something = "nothing"    # => nil NB: print prints the value and returns nil
